package demiglace.callgraph;

public class Range {
    private int start;
    private int end;

    public Range(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public int getStart() {
        return start;
    }

    public int getEnd() {
        return end;
    }

    @Override
    public String toString() {
        return "Range{" +
                "from=" + start +
                ", to=" + end +
                '}';
    }
}
